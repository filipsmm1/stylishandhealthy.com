function getRelatedPosts(currentSlug, count) {
  count = count || 3;
  return POSTS.filter(function(p) { return p.slug !== currentSlug; }).slice(0, count);
}

function renderContent(raw) {
  return raw
    .replace(/\[quote\]([\s\S]*?)\[\/quote\]/g, '<blockquote class="pull-quote">$1</blockquote>')
    .replace(/\[h2\](.*?)\[\/h2\]/g, '<h2>$1</h2>')
    .replace(/\[h3\](.*?)\[\/h3\]/g, '<h3>$1</h3>')
    .split('\n\n')
    .map(function(block) {
      block = block.trim();
      if (!block) return '';
      if (block.startsWith('<h2>') || block.startsWith('<h3>') || block.startsWith('<blockquote')) return block;
      return '<p>' + block + '</p>';
    })
    .join('\n');
}
