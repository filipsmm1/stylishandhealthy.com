function getRelatedPosts(currentSlug, count) {
  count = count || 3;
  var result = [];
  for (var i = 0; i < POSTS.length; i++) {
    if (POSTS[i].slug !== currentSlug) result.push(POSTS[i]);
    if (result.length >= count) break;
  }
  return result;
}

function renderContent(raw) {
  var html = raw
    .replace(/\[quote\]([\s\S]*?)\[\/quote\]/g, '<blockquote class="pull-quote">$1</blockquote>')
    .replace(/\[h2\](.*?)\[\/h2\]/g, '<h2>$1</h2>')
    .replace(/\[h3\](.*?)\[\/h3\]/g, '<h3>$1</h3>');
  var blocks = html.split('\n\n');
  var output = '';
  for (var i = 0; i < blocks.length; i++) {
    var block = blocks[i].trim();
    if (!block) continue;
    if (block.indexOf('<h2>') === 0 || block.indexOf('<h3>') === 0 || block.indexOf('<blockquote') === 0) {
      output += block + '\n';
    } else {
      output += '<p>' + block + '</p>\n';
    }
  }
  return output;
}
